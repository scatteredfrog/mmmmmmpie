On Pie Factory Podcast we discuss two (2) classic arcade video games per 
episode. At the end of each episode we reveal a common theme that the two (2)
games have in common. We also talk about news, take listener feedback, and share 
arcade memories from both ourselves and our listeners...all with an offbeat 
kind of delivery. And usually from a classic home gamer's perspective. (But 
always offbeat. We have no rhythm.)<br />
&nbsp;<br />
We are humbled (and proud, of course) to have been named Podcast of the Year and 
awarded the Golden Poddie in 2016 by Classic Console Gamernews.
